	*=$2000


VALIDACION?
	CLC
	LDA VARIABLE
	ADC #1
	STA VARIABLE
	
	CMP #1
	BEQ ESPRIMERO
	CMP #2
	BEQ ESSEGUNDO
	CMP #3
	BEQ ESTERCERO
	CMP #0
	BEQ NOESNINGUNO
	CMP #4 ; Accumulator greater or equal to 4?
	BCS TERMINO

ESPRIMERO
	LDX #<PRIMERO
	LDY #>PRIMERO
	JSR $C642
	JMP VALIDACION?

ESSEGUNDO
	LDX #<SEGUNDO
	LDY #>SEGUNDO
	JSR $C642
	JMP VALIDACION?

ESTERCERO
	LDX #<TERCERO
	LDY #>TERCERO
	JSR $C642
	JMP VALIDACION?

NOESNINGUNO
	LDX #<NOESNINGUNOB
	LDY #>NOESNINGUNOB
	JSR $C642
	JMP VALIDACION?
	
TERMINO
	RTS

PROCESOVALIDACION
	
INICIO
	LDA #0
	STA 710

	JSR VALIDACION?

LOOP
	JMP LOOP
	
PRIMERO
	.BYTE "PRIMERA LINEA",$9B
SEGUNDO
	.BYTE "SEGUNDA LINEA",$9B
TERCERO
	.BYTE "TERCERA LINEA",$9B
NOESNINGUNOB
	.BYTE "NINGUNA DE LAS ANTERIORES",$9B
VARIABLE
	.BYTE $00
	
LOAD_ADR	
	*=$2E0
	.WORD INICIO
	