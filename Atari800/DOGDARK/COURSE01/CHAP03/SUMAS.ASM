; CURSO DE SUMAS Y RESTAS
	*=$2000

DATOS
	;      HI  LO
	.BYTE $00,$FF,$9B
	
INICIO

; REALIZAMOS UNA SUMA

	CLC ; APAGO EL FLAG
	LDA DATOS+1 ; Agrego un 1
	ADC	#$01 ; Sumo 1
	STA DATOS+1
;	
	LDA DATOS
	ADC #0
	STA DATOS
;	
	LDX #<DATOS
	LDY #>DATOS
	JSR $C642

; REALIZAMOS UNA RESTA

	SEC			; APAGO EL FLAG
	LDA #10		; Agrego un 1
	SBC	#02	; Sumo 1
	STA DATOS+1
;	
	LDA #$00
	STA DATOS
;	
	LDX #<DATOS
	LDY #>DATOS
	JSR $C642

LOOP
	JMP LOOP
	*=$2E0
	.WORD INICIO
