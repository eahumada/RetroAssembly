;PANTALLA DE INICIO COMO PRESENTACION
;PANTALLA DE MENU PRINCIPAL
;PANTALLA DE INGRESO
;PANTALLA DE VISTA DE DATOS
;PANTALLA DE DIRECTORIO
;PANTALLA DE INFORMACION DEL SISTEMA
;16KB PARA EL CODIGO
;$C000 $DFFF - $C000 $C080(CANTIDAD DE REGISTROS - INICIO / TERMINO)
;32KB PARA EL INGRESO DE DATOS EN MEMORIA
;VARIOS ARCHIVOS QUE SE ADJUNTAN AL PRINCIPAL
	*=$2000
	.INCLUDE PANTALLA.ASM
;	.INCLUDE #D:PANTALLA.ASM PARA MAC65
CONSOLA = 53279
;WILLYSOFT(Guillermo Fuenzalida)
START
	LDA CONSOLA
	CMP #6
	BNE START
ESSTART
	LDA CONSOLA
	CMP #6
	BEQ ESSTART
FINSTART
	RTS
ESGET
	JSR $F2F8	;$F42A TODA UNA LINEA DE CARACTERES Y ESPERA UN RETURN
	CMP #27		;ES ESCAPE
	BEQ ESESC
	RTS
ESESC
	JMP MENUOPCION
INICIO
	JMP MENUOPCION
	LDA #$92
	STA 712
	LDA #0
	STA 710
	JSR PRINCIPAL
	JSR START
;MENU PRINCIPAL
MENUOPCION
	JSR MENUPRINCIPAL
	JSR ESGET
	CMP #78
	BEQ PNUEVOARTICULO
	JMP MENUOPCION
;NUEVO ARTICULO
PNUEVOARTICULO
	JSR NUEVOARTICULO
	JSR ESGET
LOOP
	JMP LOOP
	*=$2E0
	.WORD INICIO